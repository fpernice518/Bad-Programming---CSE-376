#include <stdlib.h>
#include <stdio.h>
#include "xmalloc.h"

/*===========================================================================*/
/* Main                                                                      */
/*===========================================================================*/
int main(int argc, char const *argv[])
{
  printf("\n===================================================\n");
  printf("==                  BEGIN NOMALLOC               ==\n");
  printf("===================================================\n\n");

  printf("allocating ptr 1...\n");
  char *ptr1 = xmalloc(10);
  int i = (int)&ptr1;

  i += 10;

  ptr1 = (char *) i;

  printf("freeing ptr1 + 10...\n");
  xfree(ptr1);
  printf("done freeing\n");

  return 0;
}
